export type Lang = "pt" | "en";

export const DEFAULT_LANG: Lang = "pt";

export interface Bilingual {
  pt: string;
  en: string;
}

export function pick(value: Bilingual, lang: Lang = DEFAULT_LANG): string {
  return value[lang];
}

/** Shorthand for building a Bilingual literal inline: bi("Olá", "Hello"). */
export function bi(pt: string, en: string): Bilingual {
  return { pt, en };
}

